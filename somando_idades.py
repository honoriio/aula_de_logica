# Lista com as idades 
idades = [15, 46, 75, 34, 23]

# Iniciando a variavel total 
total = 0

# Loop para somar as idades que estão dentro da lista
for idade in idades:
    print(idade)
    total = total + idade

# Print que mostra o resultado da soma das idades
print('-' * 62)
print(f'O total é {total}')
